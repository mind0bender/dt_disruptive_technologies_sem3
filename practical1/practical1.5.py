# Bitwise Operators

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("{a} & {b} is {c}".format(a=a, b=b, c=a & b))
print("{a} | {b} is {c}".format(a=a, b=b, c=a | b))
print("{a} ^ {b} is {c}".format(a=a, b=b, c=a ^ b))
print("~{a} is {b}".format(a=a, b=b, c=~a))
print("{a} << {b} is {c}".format(a=a, b=b, c=a << b))
print("{a} >> {b} is {c}".format(a=a, b=b, c=a >> b))

